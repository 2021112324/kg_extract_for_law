
import re

def extract_laws_from_file(file_path):
    """
    读取文件并提取所有《》内的法律文件名称，去重后返回列表

    Args:
        file_path: 文件路径

    Returns:
        去重后的法律文件名称列表
    """
    try:
        # 读取文件内容
        with open(file_path, 'r', encoding='utf-8') as f:
            content = f.read()

        # 使用正则表达式匹配所有《》内的内容
        # 模式解释：《([^》]+)》匹配《和》之间的任意非》字符
        laws = re.findall(r'《([^》]+)》', content)

        # 去重并添加书名号
        unique_laws = sorted(set(['《' + law + '》' for law in laws]))

        return unique_laws

    except FileNotFoundError:
        print(f"错误：文件 {file_path} 不存在")
        return []
    except Exception as e:
        print(f"读取文件时发生错误：{e}")
        return []

# 使用示例
if __name__ == "__main__":
    # 假设文件名为 f. 合规案例库（100）1207.md
    file_path = "f. 合规案例库（100）1207.md"

    # 提取法律文件
    laws_list = extract_laws_from_file(file_path)

    # 打印结果
    print(f"共找到 {len(laws_list)} 个法律文件：")
    for i, law in enumerate(laws_list, 1):
        print(f"{i:3d}. {law}")


